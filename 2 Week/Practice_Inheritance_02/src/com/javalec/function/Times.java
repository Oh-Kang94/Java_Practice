package com.javalec.function;

public class Times extends Sub{
	// Field

	// Constructor
	public Times() {

	}

	// Method

	public void times(int firstNum, int secondNum) {
		System.out.println(Integer.toString(firstNum) + " X " + Integer.toString(secondNum) + " = "
				+ Integer.toString((firstNum * secondNum))); 
	}
}
